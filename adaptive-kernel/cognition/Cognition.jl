"""
    integrate_autonomous_planner - Integrate AutonomousPlanner into cognitive loop
    
    This enables self-improvement by generating learning plans
"""
function integrate_autonomous_planner(
    engine::CognitiveEngine,
    focus_areas::Vector{String}=String[]
)::AutonomousPlanner.LearningPlan
    
    # Create a learning plan based on current performance
    if isempty(focus_areas)
        # Default focus areas based on agent performance
        focus_areas = String[]
        
        # Check which agents need improvement
        if engine.executor_performance.accuracy < 0.7
            push!(focus_areas, "execution_accuracy")
        end
        if engine.strategist_performance.accuracy < 0.7
            push!(focus_areas, "strategic_planning")
        end
        if engine.auditor_performance.accuracy < 0.7
            push!(focus_areas, "critical_analysis")
        end
    end
    
    # Generate learning journey
    plan = AutonomousPlanner.generate_learning_journey(
        focus_areas;
        module_count=AutonomousPlanner.DEFAULT_MODULE_COUNT
    )
    
    # Store in engine
    engine.planner = plan
    
    return plan
end

"""
    process_autonomous_learning - Process next module in autonomous learning plan
"""
function process_autonomous_learning(engine::CognitiveEngine)::Bool
    
    if engine.planner === nothing
        return false
    end
    
    # Get next module
    next_module = AutonomousPlanner.get_next_module!(engine.planner)
    
    if next_module === nothing
        # Plan complete - generate new plan
        @info "Autonomous learning plan complete, generating new plan"
        integrate_autonomous_planner(engine)
        return false
    end
    
    # Process the module (in a real system, this would trigger actual learning)
    @info "Processing autonomous learning module" module_title=next_module.title
    
    # Update progress
    AutonomousPlanner.update_module_progress!(next_module, 1.0)
    
    return true
end

