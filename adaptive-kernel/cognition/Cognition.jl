                decision.kernel_approved = true
                decision.kernel_approval_timestamp = now()
                @info "Kernel approved decision" decision=conflict.winner
            else
                # Fail-closed: deny execution
                decision.kernel_approved = false
                decision.kernel_approval_timestamp = nothing
                @warn "Kernel denied decision - fail-closed" decision=conflict.winner
            end
        catch e
            # Fail-closed on any error
            decision.kernel_approved = false
            decision.kernel_approval_timestamp = nothing
            @error "Kernel approval failed - fail-closed" error=string(e)
        end
    else
        # No kernel available - fail-closed
        decision.kernel_approved = false
        decision.kernel_approval_timestamp = nothing
        @warn "No kernel available - fail-closed (Flow Integrity)"
    end
    
    # Only execute if kernel approved (Flow Integrity)
    if !decision.kernel_approved
        # Rejected - do not execute
        outcome = DecisionOutcome(
            OUTCOME_REJECTED,
            Dict("decision" => decision.decision, "cycle" => cycle_number, "reason" => "kernel_denied"),
            Dict(),
            now()
        )
        decision.outcome = outcome
        return cycle_number
    end
    
    # =========================================================================
    # Step 6: Execute and record outcome (only if kernel approved)
    # =========================================================================
    outcome = DecisionOutcome(
        OUTCOME_EXECUTED,
        Dict("decision" => decision.decision, "cycle" => cycle_number, "approval_token" => string(approval_token !== nothing ? approval_token.id : "none")),
        Dict(),
        now()
    )
    
    # Record in tactical memory
    record_outcome!(engine.tactical, Dict(
        "decision" => decision.decision,
        "agents" => [p.agent_id for p in proposals],
        "winner" => conflict.winner,
        "outcome" => "success"
    ))
    
    # =========================================================================
    # Step 7: Process feedback
    # =========================================================================
    # Update optionality based on decision
    push!(engine.optionality.viable_actions, decision.decision)
    
    # Create cycle result for feedback
    cycle_result = Dict(
        "expected_outcome" => decision.decision,
        "actual_outcome" => decision.decision,
        "agent_confidence" => mean([p.confidence for p in proposals]),
        "expected_risks" => String[],
        "actual_risks" => String[],
        "warnings_issued" => occursin("veto", auditor_prop.decision) ? 1 : 0
    )
    
    # Process feedback for each agent
    process_cycle_feedback(engine.executor_performance, cycle_result)
    process_cycle_feedback(engine.strategist_performance, cycle_result)
    process_cycle_feedback(engine.auditor_performance, cycle_result)
    process_cycle_feedback(engine.evolution_performance, cycle_result)
    
    # CAUSAL EMOTIONAL FEEDBACK: Update emotional state based on outcome
    # This closes the loop: emotions → decisions → outcomes → emotions
    process_emotional_outcome(engine, :success, intensity=0.5)
    
    @debug "Emotional state after cycle" 
        valence=engine.affective_state.valence
        arousal=engine.affective_state.arousal
        modulation=engine.affective_state.value_modulation
    
    engine.last_cycle_timestamp = now()
    
    # Return cycle (simplified - full DecisionCycle would have more fields)
    return cycle_number
end

