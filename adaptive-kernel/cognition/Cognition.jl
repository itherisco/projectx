"""
    ApprovalToken - Cryptographic token for Flow Integrity pattern
    
    INVARIANTS:
    1. Token is generated by Kernel.approve() after validation
    2. Token contains cryptographic hash linking to specific decision
    3. Token must be validated before execution
    4. Token expires after defined TTL
"""
struct ApprovalToken
    id::UUID
    decision_hash::Vector{UInt8}  # SHA256 hash of decision content (32 bytes)
    generated_at::DateTime
    expires_at::DateTime
    kernel_signature::Vector{UInt8}  # Cryptographic signature from kernel
    approved_action::String  # The approved action identifier
    
    function ApprovalToken(
        decision_content::String,
        approved_action::String;
        ttl_seconds::Int=300  # 5 minute default TTL
    )
        now_ts = now()
        # Generate SHA256 hash of decision content
        decision_hash = sha256(decision_content)
        # Generate kernel signature (simplified - in production use proper crypto)
        kernel_sig = sha256(decision_content * string(now_ts))
        
        new(
            uuid4(),
            decision_hash,
            now_ts,
            now_ts + Dates.Second(ttl_seconds),
            kernel_sig,
            approved_action
        )
    end
end

"""
    generate_approval_token - Generate Flow Integrity approval token
"""
function generate_approval_token(
    decision::CommittedDecision,
    approved_action::String
)::ApprovalToken
    # Create deterministic content for hashing
    content = string(decision.id, decision.decision, decision.timestamp)
    return ApprovalToken(content, approved_action)
end

"""
    validate_approval_token - Validate Flow Integrity approval token
    
    Returns true if token is valid and not expired
"""
function validate_approval_token(
    token::ApprovalToken,
    decision::CommittedDecision
)::Bool
    # Check expiration
    if now() > token.expires_at
        @warn "Approval token expired" token_id=token.id
        return false
    end
    
    # Verify decision hash matches
    content = string(decision.id, decision.decision, decision.timestamp)
    expected_hash = sha256(content)
    if token.decision_hash != expected_hash
        @warn "Approval token decision hash mismatch" token_id=token.id
        return false
    end
    
    return true
end

